#!/usr/bin/python3
"""Function definition of Pascal's Triangle"""


def pascal_triangle(n):
    """List of lists is returned by represntng PA Triangle; sz n
    """
    if n <= 0:
        return []

    triangles = [[1]]
    while len(triangles) != n:
        tri = triangles[-1]
        tmp = [1]
        for i in range(len(tri) - 1):
            tmp.append(tri[i] + tri[i + 1])
        tmp.append(1)
        triangles.append(tmp)
    return triangles
